from library import gauss_jordan, lu_decomposition


def main():
    # -------------------------------*  matrix and vectors *-------------------------------------------------
    Amatrix = [
        [1, -1, 4, 0, 2, 9],
        [0, 5, -2, 7, 8, 4],
        [1, 0, 5, 7, 3, -2],
        [6, -1, 2, 3, 0, 8],
        [-4, 2, 0, 5, -5, 3],
        [0, 7, -1, 5, 4, -2],
    ]
    Bvector = [19, 2, 13, -7, -9, 2]

    # -------------------* Solution *-------------------------------
    p = gauss_jordan(Amatrix, Bvector)
    q = lu_decomposition(Amatrix, Bvector)

    print("Gauss-Jordan x = {}\n".format(p))
    print("LU-decomposition x = {}".format(q))


if __name__ == "__main__":
    main()

